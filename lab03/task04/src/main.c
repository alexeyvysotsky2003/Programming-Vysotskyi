#define N 1011 // задане 4-значне число
#define P 2 // тип системи счислення

int main() {

	int n1 = N / 1000; // виокремлення першої цифри заданого числа
	int n2 = N % 1000 / 100; // виокремлення другої цифри заданого числа
	int n3 = N % 100 / 10; // виокремлення третьої цифри заданого числа
	int n4 = N % 10; // виокремлення четвертої цифри заданого числа
	int result = n1 * P * P * P + n2 * P * P + n3 * P + n4; // задане число в десятковій системі счислення
	
	return 0;
}
